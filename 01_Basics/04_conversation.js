console.log("======Comparision b/w same data type====")
console.log(2>1)
console.log(2<1)
console.log(2==2)
console.log(2!=2)

console.log("======Comparision b/w same different type====") //Not a good practice
console.log("2">1)//(1 can be boolean or number)
console.log("02">1)
console.log("2"<1)
console.log("2"==2)
console.log("2"!=2)


console.log("======Null====") //avoid this type of comparasion
console.log(null>0)
console.log(null==0)
console.log(null>=0)

console.log("======Undefined====")  //avoid this type of comparasion
console.log(undefined>0)
console.log(undefined<0)
console.log(undefined==0)
console.log(undefined>=0)

console.log("======Triple Equal====")
console.log(2===2)
console.log("2"==="2")
console.log("2"===2)
